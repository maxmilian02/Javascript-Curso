let idade = 18;

if (idade >= 18) {
  console.log("Você é maior de idade.");
} else {
  console.log("Você é menor de idade.");
}

// Neste exemplo, utilizamos uma estrutura condicional if-else para verificar se a variável idade é maior ou igual a 18. Se a condição for verdadeira, exibimos a mensagem "Você é maior de idade." no console. Caso contrário, exibimos a mensagem "Você é menor de idade."

