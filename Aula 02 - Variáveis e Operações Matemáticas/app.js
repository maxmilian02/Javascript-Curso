// Declaração de variáveis
let a = 5;
let b = 3;

// Operações matemáticas
let soma = a + b;
let subtracao = a - b;
let multiplicacao = a * b;
let divisao = a / b;

// Exibindo os resultados
console.log("Soma:", soma);
console.log("Subtração:", subtracao);
console.log("Multiplicação:", multiplicacao);
console.log("Divisão:", divisao);

// Neste exemplo, introduzimos o conceito de variáveis e mostramos algumas operações matemáticas básicas. As variáveis a e b são declaradas e, em seguida, realizamos algumas operações com elas, armazenando os resultados em outras variáveis. Por fim, exibimos os resultados no console.