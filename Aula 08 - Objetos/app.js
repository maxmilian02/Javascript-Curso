// Criando um objeto
let pessoa = {
    nome: "João",
    idade: 30,
    profissao: "Engenheiro",
  };
  
  // Acessando propriedades do objeto
  console.log("Nome:", pessoa.nome);
  console.log("Idade:", pessoa.idade);
  console.log("Profissão:", pessoa.profissao);
  
  // Modificando propriedades do objeto
  pessoa.idade = 31;
  console.log("Nova idade:", pessoa.idade);

// Neste exemplo, criamos um objeto chamado pessoa que possui algumas propriedades, como nome, idade e profissao. Utilizamos a notação de ponto (.) para acessar as propriedades do objeto e exibi-las no console. Também demonstramos como modificar uma propriedade do objeto atribuindo um novo valor a ela.